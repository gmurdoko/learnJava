package com.dicoding.javafundamental.operator;

public class OperatorAritmatika {
    public static void main(String[] args) {
        System.out.println("Operasi Penjumlaha");
        int hasilPenjumlahan = 5 + 1;
        System.out.println("Hasil  5 + 1 = " + hasilPenjumlahan + "\n");
        System.out.println("Hasil Pengurangan");
        int hasilPengurangan = 4 - 1;
        System.out.println("Hasil 4-1 = " + hasilPengurangan + "\n");
        System.out.println("Hasil Pengalian");
        int hasilPengalian = 5 * 5;
        System.out.println("Hasil 5*5 =  " + hasilPengalian + "\n");
        System.out.println("Hasil Pembagian");
        int hasilPembagian = 20 / 1;
        System.out.println("Hasil 20/2 = " + hasilPembagian + "\n");
        System.out.println("Operasi Habis bagi");
        int hasilSisa = 8 % 2;
        System.out.println("Hasil 8%2 =" + hasilSisa );
        int hasilSisaLain = 9 % 2;
        System.out.println("Hasil 9%2=" + hasilSisaLain + "\n");
    }
}
